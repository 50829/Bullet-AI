create or replace function public.resolve_habit_checkin_reference()
returns trigger
language plpgsql
set search_path = ''
as $$
declare
  resolved_habit_id bigint;
  resolved_client_id text;
begin
  if new.habit_client_id is not null then
    select id, client_id
      into resolved_habit_id, resolved_client_id
    from public.habits
    where user_id = new.user_id
      and client_id = new.habit_client_id;
  elsif new.habit_id is not null then
    select id, client_id
      into resolved_habit_id, resolved_client_id
    from public.habits
    where user_id = new.user_id
      and id = new.habit_id;
  end if;

  if resolved_habit_id is null then
    raise foreign_key_violation using
      message = 'Habit check-in must reference a habit owned by the same user';
  end if;

  new.habit_id = resolved_habit_id;
  new.habit_client_id = resolved_client_id;
  return new;
end;
$$;

revoke all on function public.resolve_habit_checkin_reference()
  from public, anon, authenticated;

create trigger resolve_habit_checkin_reference
  before insert or update of user_id, habit_id, habit_client_id
  on public.habit_checkins
  for each row execute function public.resolve_habit_checkin_reference();

create or replace function public.enforce_habit_started_on_immutable()
returns trigger
language plpgsql
set search_path = ''
as $$
begin
  if new.started_on is distinct from old.started_on then
    raise check_violation using
      message = 'Habit started_on is immutable';
  end if;
  return new;
end;
$$;

revoke all on function public.enforce_habit_started_on_immutable()
  from public, anon, authenticated;

create trigger enforce_habit_started_on_immutable
  before update of started_on on public.habits
  for each row execute function public.enforce_habit_started_on_immutable();

