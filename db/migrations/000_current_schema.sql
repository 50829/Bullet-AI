-- Core tables -----------------------------------------------------------------

create table public.profiles (
  user_id uuid primary key references auth.users(id) on delete cascade,
  username text,
  preferred_language text not null default 'zh'
    check (preferred_language in ('zh', 'en')),
  accent_color text not null default 'sage'
    check (accent_color in ('sage', 'green', 'purple', 'amber')),
  color_scheme text not null default 'system'
    check (color_scheme in ('system', 'light', 'dark')),
  completed_goal_retention text not null default 'next_day'
    check (completed_goal_retention in ('instant', 'next_day', 'never')),
  week_starts_on text not null default 'auto'
    check (week_starts_on in ('auto', 'monday', 'sunday', 'saturday')),
  version bigint not null default 1 check (version >= 1),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint profiles_username_not_blank
    check (username is null or btrim(username) <> '')
);

create table public.moments (
