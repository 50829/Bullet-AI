-- Fixed server-side AI rate limit. Clients cannot choose an identity, window,
-- or quota. Old events are pruned opportunistically for the current user.
create or replace function public.reserve_ai_usage_event()
returns boolean
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_user_id uuid := auth.uid();
  event_count integer;
begin
  if current_user_id is null then
    return false;
  end if;

  perform pg_catalog.pg_advisory_xact_lock(
    pg_catalog.hashtextextended('ai_usage_events:' || current_user_id::text, 0)
  );

  delete from public.ai_usage_events
  where user_id = current_user_id
    and created_at < now() - interval '24 hours';

  select count(*) into event_count
  from public.ai_usage_events
  where user_id = current_user_id
    and created_at >= now() - interval '1 hour';

  if event_count >= 20 then
    return false;
  end if;

  insert into public.ai_usage_events (user_id) values (current_user_id);
  return true;
end;
$$;

revoke all on function public.reserve_ai_usage_event() from public, anon;
grant execute on function public.reserve_ai_usage_event() to authenticated;

