create or replace function public.reserve_ai_usage_event(
  p_user_id uuid,
  p_window_start timestamptz,
  p_limit int
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  event_count int;
begin
  if auth.uid() is null or auth.uid() <> p_user_id then
    return false;
  end if;

  select count(*) into event_count
  from public.ai_usage_events
  where user_id = p_user_id
    and created_at >= p_window_start;

  if event_count >= p_limit then
    return false;
  end if;

  insert into public.ai_usage_events (user_id)
  values (p_user_id);

  return true;
end;
$$;

grant execute on function public.reserve_ai_usage_event(uuid, timestamptz, int)
  to authenticated;

create or replace function public.resolve_habit_checkin_reference()
returns trigger as $$
begin
  if new.habit_client_id is null and new.habit_id is not null then
    select client_id into new.habit_client_id
    from public.habits
    where id = new.habit_id and user_id = new.user_id;
  end if;

  if new.habit_client_id is not null then
    select id into new.habit_id
    from public.habits
    where client_id = new.habit_client_id and user_id = new.user_id;
  end if;

  return new;
end;
$$ language plpgsql;

