update public.habit_checkins checkins
set
  client_id = coalesce(checkins.client_id, 'habit-checkin-' || checkins.id::text),
  habit_client_id = coalesce(checkins.habit_client_id, habits.client_id),
  checked = coalesce(checkins.checked, true),
  updated_at = coalesce(checkins.updated_at, checkins.created_at, timezone('utc'::text, now()))
from public.habits habits
where checkins.habit_id = habits.id
  and (
    checkins.client_id is null
    or checkins.habit_client_id is null
    or checkins.checked is null
    or checkins.updated_at is null
  );

insert into public.habit_checkins (
  user_id,
  habit_id,
  client_id,
  habit_client_id,
  checked_on,
  checked,
  created_at,
  updated_at
)
select
  habits.user_id,
  habits.id,
  'habit-checkin-legacy-' || habits.id::text || '-' || habits.last_checkin::date::text,
  habits.client_id,
  habits.last_checkin::date,
  true,
  coalesce(habits.last_checkin, timezone('utc'::text, now())),
  coalesce(habits.last_checkin, timezone('utc'::text, now()))
from public.habits habits
where habits.last_checkin is not null
  and not exists (
    select 1
    from public.habit_checkins checkins
    where checkins.user_id = habits.user_id
      and checkins.habit_id = habits.id
      and checkins.checked_on = habits.last_checkin::date
  );

